package com.pemng.serviceSystem.common.office.pojo;

/**
 * 标的对象
 * 
 * @author jenson
 * 
 */
public class obinfo {
	private String obname;// 标的名称
	private String brm;// 品牌型号
	private String stm;// st_m
	private String buydate;// 购置时间
	private String parts;// 配件
	private String yprice;// 原购单价
	private String isuse;// 是否能够正常用
	private String otremark;// 其他
	private String unit; //单位
	private double amount; //数量

	public String getObname() {
		return obname;
	}

	public void setObname(String obname) {
		this.obname = obname;
	}

	public String getBrm() {
		return brm;
	}

	public void setBrm(String brm) {
		this.brm = brm;
	}

	public String getStm() {
		return stm;
	}

	public void setStm(String stm) {
		this.stm = stm;
	}

	public String getBuydate() {
		return buydate;
	}

	public void setBuydate(String buydate) {
		this.buydate = buydate;
	}

	public String getParts() {
		return parts;
	}

	public void setParts(String parts) {
		this.parts = parts;
	}

	public String getYprice() {
		return yprice;
	}

	public void setYprice(String yprice) {
		this.yprice = yprice;
	}

	public String getIsuse() {
		return isuse;
	}

	public void setIsuse(String isuse) {
		this.isuse = isuse;
	}

	public String getOtremark() {
		return otremark;
	}

	public void setOtremark(String otremark) {
		this.otremark = otremark;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public double getAmount() {
		return amount;
	}

	public void setAmount(double amount) {
		this.amount = amount;
	}

	 

}
